// WAP to print even numbers in a range of 1 to 200

 class EvenNumSeries
{
     public static void main(String args[])
      {    
         System.out.println( "Even numbers in a range of 1 to 200 are : ");

          for(int i=1; i<= 200; i++)
           {
              if( i %2==0)
                 {
                     System.out.println( i );
                 }

            }
       }
 }  