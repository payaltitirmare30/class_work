// WAP to get sum of first 10 natural numbers

  class SumOfNatural
{
     public static void main(String args[])
      {    
         int sum =0;

          for(int i=1; i<= 10; i++)
          {
            sum = sum +i;
           }

        System.out.println("sum of first 10 natural numbers is :" +sum);

       }
 }  